import Box from "@mui/material/Box";
import Link from "@mui/material/Link";
import LightGallery from "lightgallery/react";

// Lightgallery.
import "lightgallery/css/lightgallery.css";
import "../../../styles/lg-overrides.css";

export default function ImageList({ onInit, setIndex }) {
  const wrapperStyles = {
    ".lg-react-element": {
      flexWrap: "wrap",
      overflow: "visible",
      display: "inline-flex",
      transform: "rotate(-0.5deg)",
      backfaceVisibility: "hidden",
      width: { xs: "120vw", md: "175vw" },
    },
  };

  const linkStyles = {
    cursor: "pointer",
    overflow: "hidden",
    position: "relative",
    aspectRatio: "1 / 1",
    display: "inline-flex",
    width: { xs: "40vw", md: "25vw" },
    "&:before": {
      content: "''",
      opacity: 0.3,
      width: "100%",
      height: "100%",
      position: "absolute",
      transition: "all 0.5s",
      bgcolor: "nice-black.main",
    },
    "&.active:before": {
      opacity: 0.5,
      bgcolor: "sunset-orange.main",
    },
  };

  const imageStyles = {
    width: "100%",
    height: "100%",
    objectFit: "cover",
  };

  const handleClick = (event) => {
    const selected = event.currentTarget.getAttribute("data-key");
    const index = parseInt(selected, 10);
    setIndex(index);
  };

  return (
    <Box sx={wrapperStyles}>
      <LightGallery onInit={onInit} counter={false} download={false}>
        {itemData.map((item, index) => (
          <Link
            key={index}
            sx={linkStyles}
            data-key={index}
            data-src={item.img}
            onClick={handleClick}
          >
            <Box
              loading="lazy"
              component="img"
              alt={item.title}
              sx={imageStyles}
              src={`${item.img}?w=800&fit=cover&auto=format`}
              srcSet={`${item.img}?w=800&fit=cover&auto=format&dpr=2 2x`}
            />
          </Link>
        ))}
      </LightGallery>
    </Box>
  );
}

